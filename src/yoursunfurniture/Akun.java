    public boolean readUser(String user) {
        boolean isAuthenticated = false;
        Database db = new Database();

        try {
            db.openConnection();
            String query = "SELECT * FROM admin WHERE user_id = ?";
            try (java.sql.PreparedStatement preparedStatement = db.getConnection().prepareStatement(query)) {
                preparedStatement.setString(1, user);
                try (java.sql.ResultSet resultSet = preparedStatement.executeQuery()) {
                    if (resultSet.next()) {
                        this.id_admin = resultSet.getString("id_admin");
                        this.nama_lengkap = resultSet.getString("nama_lengkap");
                        this.alamat = resultSet.getString("alamat");
                        this.no_telepon = resultSet.getString("no_telepon");
                        this.email = resultSet.getString("email");

                        isAuthenticated = true;
                        
                        jTextField4.setText(id_admin);
                        jTextField5.setText(nama_lengkap);
                        jTextField3.setText(alamat);
                        jTextField1.setText(no_telepon);
                        jTextField2.setText(email);
                    }
                }
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }

        return isAuthenticated;
    }
    
