    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
    String id_admin = jTextField4.getText();
    String nama = jTextField5.getText();
    String alamat = jTextField3.getText();
    String noHp = jTextField1.getText();
    String email = jTextField2.getText();

    if (id_admin.isEmpty() || nama.isEmpty() || alamat.isEmpty() || noHp.isEmpty() || email.isEmpty()) {
        JOptionPane.showMessageDialog(null, "Silahkan isi data terlebih dahulu.", "Peringatan", JOptionPane.WARNING_MESSAGE);
    } else {
        try {
            int parsedIdAdmin = Integer.parseInt(id_admin);
            int parsedNoHp = Integer.parseInt(noHp);

            Admin adm = new Admin(user_id, null, parsedIdAdmin, nama, alamat, parsedNoHp, email);
            boolean createSuccess = adm.createOrUpdateDataAdmin();

            if (createSuccess) {
                JOptionPane.showMessageDialog(null, "Data Admin berhasil diubah!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "ID Admin dan No. Telepon harus berupa angka.", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }

    }//GEN-LAST:event_jButton1ActionPerformed

